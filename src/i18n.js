function normalizeLanguage(language) {
  return language === 'en' ? 'en' : 'de';
}

function interpolate(template, variables = {}) {
  return String(template).replace(/\{\{(\w+)\}\}/g, (_match, key) => {
    const value = variables[key];
    return value === undefined || value === null ? '' : String(value);
  });
}

export function translate(language, key, variables = {}) {
  const lang = normalizeLanguage(language);
  const table = messages[lang] || messages.de;
  const fallback = messages.de[key] || key;
  return interpolate(table[key] || fallback, variables);
}

export function translateGroup(language, group) {
  return normalizeLanguage(language) === 'en' ? (groupTranslations[group] || group) : group;
}

const deviceLabelReplacementMap = new Map(deviceLabelReplacements);
const deviceLabelPattern = new RegExp(
  deviceLabelReplacements
    .map(([source]) => source)
    .sort((a, b) => b.length - a.length)
    .map((source) => source.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'))
    .join('|'),
  'g',
);

export function translateDeviceLabel(language, label) {
  if (normalizeLanguage(language) !== 'en') return label;
  const sourceLabel = String(label);
  if (deviceLabelTranslations[sourceLabel]) return deviceLabelTranslations[sourceLabel];
  return sourceLabel.replace(deviceLabelPattern, (source) => deviceLabelReplacementMap.get(source) || source);
}

export function translatePlatform(language, platform) {
  if (normalizeLanguage(language) !== 'en') return platform;
  return platformTranslations[platform] || platform;
}

export function translateRuntimeText(language, value) {
  if (normalizeLanguage(language) !== 'en' || !value) return value;
  let translated = String(value);
  for (const [source, target] of runtimeReplacements) translated = translated.replaceAll(source, target);
  return translated;
}

export function getStoredLanguage() {
  try {
    return normalizeLanguage(localStorage.getItem(STORAGE_KEY));
  } catch (_error) {
    return 'de';
  }
}

export function storeLanguage(language) {
  const next = normalizeLanguage(language);
  try {
    localStorage.setItem(STORAGE_KEY, next);
  } catch (_error) {
    // Local storage can be unavailable in restricted browser contexts.
  }
  if (typeof document !== 'undefined') document.documentElement.lang = next;
  return next;
}

if (typeof window !== 'undefined') {
  window.eedtoyI18n = {
    setLanguage: storeLanguage,
    getLanguage: getStoredLanguage,
  };
}
